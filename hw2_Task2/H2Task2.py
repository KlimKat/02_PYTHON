# 1) –í–≤–æ–¥–∏—Ç—Å—è —Å—É–º–º–∞ —á–∏—Å–µ–ª S –∏ –ø—Ä–æ–∏–∑–≤–µ–¥–µ–Ω–∏–µ —á–∏—Å–µ–ª P.
# 2) –ò–Ω–∏—Ü–∏–∞–ª–∏–∑–∏—Ä—É–µ—Ç—Å—è —Ñ–ª–∞–≥ found, –∏ –Ω–∞—á–∞–ª—å–Ω—ã–µ –∑–Ω–∞—á–µ–Ω–∏—è –¥–ª—è 
