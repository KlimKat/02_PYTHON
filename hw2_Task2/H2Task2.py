# 4) –ï—Å–ª–∏ —Ç–µ–∫—É—â–∏–µ X –∏ Y —É–¥–æ–≤–ª–µ—Ç–≤–æ—Ä—è—é—Ç —É—Å–ª–æ–≤–∏—è–º ùëã+ùëå=ùëÜ –∏ ùëã‚ãÖùëå=ùëÉ, 
# —É—Å—Ç–∞–Ω–∞–≤–ª–∏–≤–∞–µ–º —Ñ–ª–∞–≥ flag –≤ True –∏ —Å–æ—Ö—Ä–∞–Ω—è–µ–º –∑–Ω–∞—á–µ–Ω–∏—è ùëã –∏ ùëå.
# 5) –ó–∞–≤–µ—Ä—à–∞–µ–º –æ–±–∞ —Ü–∏–∫–ª–∞, –µ—Å–ª–∏ —Ñ–ª–∞–≥ flag —É—Å—Ç–∞–Ω–æ–≤–ª–µ–Ω –≤ True.
# 6) –í—ã–≤–æ–¥–∏–º –Ω–∞–π–¥–µ–Ω–Ω—É—é –ø–∞—Ä—É —á–∏—Å–µ–ª –∏–ª–∏ —Å–æ–æ–±—â–µ–Ω–∏–µ –æ –Ω–µ—É–¥–∞—á–µ, –µ—Å–ª–∏ –ø–∞—Ä–∞ –Ω–µ –Ω–∞–π–¥–µ–Ω–∞.


# –í–≤–æ–¥ —Å—É–º–º—ã –∏ –ø—Ä–æ–∏–∑–≤–µ–¥–µ–Ω–∏—è —á–∏—Å–µ–ª
S = int(input("–í–≤–µ–¥–∏—Ç–µ —Å—É–º–º—É —á–∏—Å–µ–ª S: "))
P = int(input("–í–≤–µ–¥–∏—Ç–µ –ø—Ä–æ–∏–∑–≤–µ–¥–µ–Ω–∏–µ —á–∏—Å–µ–ª P: "))

# –ò–Ω–∏—Ü–∏–∞–ª–∏–∑–∞—Ü–∏—è —Ñ–ª–∞–≥–∞ –∏ –ø–µ—Ä–µ–º–µ–Ω–Ω—ã—Ö
flag = False
X = 1
Y = 1

# –ü–µ—Ä–µ–±–æ—Ä –≤—Å–µ—Ö –≤–æ–∑–º–æ–∂–Ω—ã—Ö –∑–Ω–∞—á–µ–Ω–∏–π –¥–ª—è X –∏ Y
i = 1
while i <= 1000 and not flag:
    j = 1
    while j <= 1000 and not flag:
        if i + j == S and i * j == P:
            X = i
            Y = j
            flag = True
        j += 1
    i += 1

# –í—ã–≤–æ–¥ —Ä–µ–∑—É–ª—å—Ç–∞—Ç–∞
if flag:
    print(f"–ó–∞–¥—É–º–∞–Ω–Ω—ã–µ —á–∏—Å–ª–∞: {X} –∏ {Y}")
else:
    print("–ù–µ —É–¥–∞–ª–æ—Å—å –Ω–∞–π—Ç–∏ —Ç–∞–∫–∏–µ —á–∏—Å–ª–∞.")