# 3) –ò—Å–ø–æ–ª—å–∑—É–µ—Ç—Å—è –≤–ª–æ–∂–µ–Ω–Ω—ã–π —Ü–∏–∫–ª while –¥–ª—è –ø–µ—Ä–µ–±–æ—Ä–∞ –≤—Å–µ—Ö –≤–æ–∑–º–æ–∂–Ω—ã—Ö –∑–Ω–∞—á–µ–Ω–∏–π 
