# Задача 10: 
# На столе лежат n монеток. 
# Некоторые из них лежат вверх
# решкой, а некоторые – гербом. 
# Определите минимальное число
# монеток, которые нужно перевернуть, чтобы все монетки были
# повернуты вверх одной и той же стороной. 
# Выведите минимальное
# количество монет, которые нужно перевернуть.
# Пример:
# 5 -> 1 0 1 1 0 2

# Алгоритм:
# 1) Вводим количество монет и их состояние (решка или герб).
# 2) Подсчитываем количество монет, лежащих вверх решкой (0).
# 3) Подсчитываем количество монет, лежащих вверх гербом (1).
# 4) Минимальное количество монет, которые нужно перевернуть, 
# будет равно меньшему из этих двух чисел.

# Этот код работает следующим образом:
# 1) Задается количество монет n.
# 2) Задается список состояний монет coins (0 для решки и 1 для герба).
# 3) Инициализируются счетчики для решки (count_heads) и герба (count_tails).
# 4) Цикл for проходит по всем монетам и подсчитывает количество монет, 
#лежащих вверх решкой и гербом.
# 5) Определяется минимальное количество монет, которые нужно перевернуть.
# 6) Выводится результат.


#Вводим кол-во монет
coins_n = 10
# Вводим состояния каждой монеты
coins_sides = [1, 0, 1, 1, 0, 0, 1, 0, 0, 1]

# Инициализируем счетчики для решки и герба
count_0 = 0  # Решка (0)
count_1 = 0  # Герб (1)

# Проходим по всем монетам
for i in range(coins_n):
    if coins_sides[i] == 0:
        count_0 += 1
    else:
        count_1 += 1

# Определяем минимальное количество монет, которые нужно перевернуть
coins_min_flips = min(count_0, count_1)

# Выводим результат
print(f"Минимальное количество монет, которые нужно перевернуть: {coins_min_flips}")

